#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	num;

	i = 0;
	j = 1;
	num = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			j = -1 * j;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		num = num * 10 + str[i] - 48;
		i++;
	}
	if (j == -1)
		num = -1 * num;
	return (num);
}
