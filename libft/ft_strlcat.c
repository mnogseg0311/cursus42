#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	n;

	if (dstsize <= ft_strlen(dst))
		return (dstsize + ft_strlen(src));
	n = ft_strlen(dst);
	i = n;
	j = 0;
	while (i < dstsize - 1 && src[j] != '\0')
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (n + ft_strlen(src));
}
